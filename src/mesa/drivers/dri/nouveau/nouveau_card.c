{
	int i;
	for(i=0;i<sizeof(nouveau_card_list)/sizeof(nouveau_card)-1;i++)
		if (nouveau_card_list[i].id==(device_id&0xfff0))
			break;
	return &(nouveau_card_list[i]);
}


